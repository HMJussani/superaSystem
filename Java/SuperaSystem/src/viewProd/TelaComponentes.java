    private void getDados() {
        tipo = cbTipo.getSelectedItem().toString();
        fabricante = txtFabricante.getText();
        modelo = txtModelo.getText();
        capacidade = txtCapacidade.getText();
        detalhe = txtDetalhe.getText();
    }

    private void novoComponente(String tipo, String fabricante, String modelo, String capa, String detalhe) {
        ComponenteDAO comp = new ComponenteDAO();
        if (comp.novoComponente(tipo, fabricante, modelo, capa, detalhe)) {

            JOptionPane.showMessageDialog(null, "Componente inserido com sucesso.");
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao inserir componente.");
        }
        limpar();
    }

    private void limpar() {
        txtDetalhe.setText("");
        txtFabricante.setText("");
        txtModelo.setText("");
        txtCapacidade.setText("");
        limpaTabela();
    }

