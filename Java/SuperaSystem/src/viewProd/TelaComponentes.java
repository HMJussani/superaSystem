    private void setarTabela(String tipo) {
        ComponenteDAO compDao = new ComponenteDAO();
        DefaultTableModel model = (DefaultTableModel) tbComp.getModel();
        model.setRowCount(0);
        ArrayList<ComponenteBean> compList = compDao.pesquisarComp("tipo", tipo);
        for (int i = 0; i < compList.size(); i++) {
            model.addRow(new Object[]{
                compList.get(i).getTipo(),
                compList.get(i).getFabricante(),
                compList.get(i).getModelo(),
                compList.get(i).getCapacidade()
            });
        }
    }

    private void setarCampos(int linha) {        
        String valor1 = tbComp.getValueAt(linha, 0).toString();        
        String valor2 = tbComp.getValueAt(linha, 2).toString();
        ComponenteDAO compDao = new ComponenteDAO();
        ArrayList<ComponenteBean> compList = compDao.pesquisarComp("tipo", valor1, "modelo", valor2);
        txtDetalhe.setText(compList.get(0).getDetalhe());
        txtFabricante.setText(compList.get(0).getFabricante());
        txtModelo.setText(compList.get(0).getModelo());
        txtCapacidade.setText(compList.get(0).getCapacidade());

    }

    private void limpaTabela() {
        DefaultTableModel model = (DefaultTableModel) tbComp.getModel();
        model.setRowCount(0);
    }

